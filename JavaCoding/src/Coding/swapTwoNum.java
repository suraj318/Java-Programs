package Coding;

public class swapTwoNum {
	public static void main(String[] args) {
		
		int a=3;
		int b=2;
		
		System.out.println("a : "+a);
		System.out.println("b : "+b);
		
		System.out.println();
		
		a=a+b;
		b=a-b;
		a=a-b;
		
		System.out.println("a : "+a);
		System.out.println("b : "+b);
		
	}

}
